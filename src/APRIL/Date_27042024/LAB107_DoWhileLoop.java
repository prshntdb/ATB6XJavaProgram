package APRIL.Date_27042024;

public class LAB107_DoWhileLoop {
    public static void main(String[] args) {
        // While -> int ->Condition -> body -> increment/decrement

        // Do While
        // int -> body -> Condition -> increment/decrement

        int a=0;
        do {
            System.out.println(a);
            a--;
        }while(a>-5);
    }
}
