package APRIL.Date_27042024;

public class LAB088_ForLoops {
    public static void main(String[] args) {
        //For Loops and While and Do While
        //Loops
        //to perform a task multiple times
        //for(A:B:C)
        // A-> initialization -> where you want to start - int i=0
        // B-> condition (Where you want to stop)- i<10
        // C-> how you want to do increment/decrement/ updation -i++,i=i+1
        System.out.println(1);
        System.out.println(2);
        System.out.println(3);
        System.out.println(4);
        System.out.println(5);
        System.out.println("....");
        System.out.println(10);
        for (int i=0;i<10;i++)
            System.out.println("Code which you want to execute multiples times");
    }
}
