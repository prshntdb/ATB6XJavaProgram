public class LAB035_ForLoopsWithEvenodd {
    public static void main(String[] args) {
        for (int i = 1; i <=50 ; i++) {
            if (i%2==0){
                System.out.println("Even number"+i);
            }else {
                System.out.println("ODD Number"+i);
            }


        }
    }
}
